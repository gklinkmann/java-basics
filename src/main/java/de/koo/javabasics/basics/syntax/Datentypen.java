package de.koo.javabasics.basics.syntax;

public class Datentypen {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		/*
		 * Primitive Datentypen
		 */
		
		// Datentypen für Zahlen
		int zahl1; // 0;1;-1;111 Ganzzahlen
		long zahl3;
		
		float zahl4;
		double zahl2; // 0,2; -0,2; 0,33333333333 Fließkommazahlen  

		// Datentypen für Zeichen
		char zeichen;
		
		// Datentypen für Boolsche Werte
		boolean wahr;
	}

}
