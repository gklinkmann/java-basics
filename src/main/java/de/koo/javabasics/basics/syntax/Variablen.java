package de.koo.javabasics.basics.syntax;

public class Variablen {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		/*
		 * Primitive Datentypen
		 * 
		 * byte < short < int < long 
		 * float < double
		 */
		
		// Datentypen für Zahlen
		int zahl1=-1; // 0;1;-1;111 Ganzzahlen
		long zahl3=11111;
		
		float zahl4=1.4f;
		double zahl2=0.1; // 0,2; -0,2; 0,33333333333 Fließkommazahlen  

		// Datentypen für Zeichen
		char zeichen='a';
		
		// Datentypen für Boolsche Werte
		// Deklaration
		boolean wahr;
		
		// Wertzuweisung
		wahr=true;
		wahr=false;
		wahr=true;
	}

}
